package com.sonar.it.vbnet;

import com.sonar.orchestrator.build.BuildResult;
import com.sonar.orchestrator.http.HttpMethod;
import java.io.IOException;
import java.nio.file.Path;
import org.junit.jupiter.api.Test;
import org.junit.jupiter.api.extension.ExtendWith;
import org.junit.jupiter.api.io.TempDir;

import static com.sonar.it.vbnet.Tests.ORCHESTRATOR;
import static com.sonar.it.vbnet.Tests.getMeasureAsInt;
import static org.assertj.core.api.Assertions.assertThat;

@ExtendWith(Tests.class)
class AutoGeneratedTest {

  @TempDir
  private static Path temp;

  private final String PROJECT_NAME = "VbAutoGeneratedFiles";

  @Test
  void autogenerated_code_with_analyzeGenerated_false() throws Exception {
    analyzeCoverageTestProject(PROJECT_NAME, false);

    // only MyModule.vb is scanned
    assertThat(getMeasureAsInt(PROJECT_NAME, "files")).isEqualTo(1);
    assertThat(getMeasureAsInt(PROJECT_NAME, "ncloc")).isEqualTo(7);
    assertThat(getMeasureAsInt(PROJECT_NAME, "code_smells")).isEqualTo(3);
  }

  @Test
  void autogenerated_code_with_analyzeGenerated_true() throws Exception {
    analyzeCoverageTestProject(PROJECT_NAME, true);

    assertThat(getMeasureAsInt(PROJECT_NAME, "files")).isEqualTo(6);
    assertThat(getMeasureAsInt(PROJECT_NAME, "ncloc")).isEqualTo(50);
    assertThat(getMeasureAsInt(PROJECT_NAME, "code_smells")).isEqualTo(12);
  }

  @Test
  void autogenerated_files_are_logged() throws Exception {
    BuildResult result = analyzeCoverageTestProject(PROJECT_NAME, false, "sonar.verbose", "true");

    assertThat(result.getLogsLines(x -> x.contains("was recognized as generated"))).hasSize(7);
    assertThat(result.getLogs()).contains("GeneratedCode.vb' was recognized as generated");
  }

  private BuildResult analyzeCoverageTestProject(String projectName, boolean analyzeGenerated, String... keyValues) throws IOException {
    // an alternative to using the SQ API is passing the setting directly to the SonarScanner for .NET (S4MSB),
    // however the S4MSB is not reading correctly the input parameters (SonarSource/sonar-scanner-msbuild#699)
    ORCHESTRATOR.getServer()
      .newHttpCall("/api/settings/set")
      .setAdminCredentials()
      .setMethod(HttpMethod.POST)
      .setParam("key", "sonar.vbnet.analyzeGeneratedCode")
      .setParam("value", String.valueOf(analyzeGenerated))
      .execute();

    return Tests.analyzeProject(temp, projectName, keyValues);
  }
}
