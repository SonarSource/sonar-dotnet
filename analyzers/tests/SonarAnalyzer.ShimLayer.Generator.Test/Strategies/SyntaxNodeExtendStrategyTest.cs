    [TestMethod]
    public void Generate_ClassDeclarationSyntax_ParameterList()
    {
        var sut = new SyntaxNodeExtendStrategy(typeof(ClassDeclarationSyntax), [
            new(typeof(ClassDeclarationSyntax).GetMember(nameof(ClassDeclarationSyntax.ParameterList))[0], false),
            new(typeof(ClassDeclarationSyntax).GetMember(nameof(ClassDeclarationSyntax.SemicolonToken))[0], false),
            new(typeof(ClassDeclarationSyntax).GetMember(nameof(ClassDeclarationSyntax.Span))[0], true), // exisiting member, should be ignored
        ]);
        var result = sut.Generate([]);
        result.Should().BeIgnoringLineEndings(
            """
            //<auto-generated/>
            /*
             * SonarAnalyzer for .NET
             * Copyright (C) 2014-2025 SonarSource Sàrl
             * mailto:info AT sonarsource DOT com
             * This program is free software; you can redistribute it and/or
             * modify it under the terms of the Sonar Source-Available License Version 1, as published by SonarSource SA.
             *
             * This program is distributed in the hope that it will be useful,
             * but WITHOUT ANY WARRANTY; without even the implied warranty of
             * MERCHANTABILITY or FITNESS FOR A PARTICULAR PURPOSE.
             * See the Sonar Source-Available License for more details.
             *
             * You should have received a copy of the Sonar Source-Available License
             * along with this program; if not, see https://sonarsource.com/license/ssal/
             */

            using Microsoft.CodeAnalysis;
            using Microsoft.CodeAnalysis.CSharp;
            using Microsoft.CodeAnalysis.CSharp.Syntax;
            using Microsoft.CodeAnalysis.Text;
            using System;
            using System.Collections.Immutable;

            namespace SonarAnalyzer.ShimLayer;

            public static partial class ClassDeclarationSyntaxShimExtensions
            {
                private static readonly Type WrappedType = typeof(ClassDeclarationSyntax);

                private static readonly Func<ClassDeclarationSyntax, ParameterListSyntax> ParameterListAccessor = LightupHelpers.CreateSyntaxPropertyAccessor<ClassDeclarationSyntax, ParameterListSyntax>(WrappedType, "ParameterList");

                private static readonly Func<ClassDeclarationSyntax, SyntaxToken> SemicolonTokenAccessor = LightupHelpers.CreateSyntaxPropertyAccessor<ClassDeclarationSyntax, SyntaxToken>(WrappedType, "SemicolonToken");


                extension(ClassDeclarationSyntax @this)
                {
                    public ParameterListSyntax ParameterList => (ParameterListSyntax)ParameterListAccessor(@this);
                    public SyntaxToken SemicolonToken => (SyntaxToken)SemicolonTokenAccessor(@this);
                }
            }
            """);
    }

