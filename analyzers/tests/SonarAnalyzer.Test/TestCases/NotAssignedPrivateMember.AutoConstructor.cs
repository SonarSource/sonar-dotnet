using System;

// Stub mirroring the attribute emitted by the AutoConstructor source generator
// (https://www.nuget.org/packages/AutoConstructor). The generator emits it into the GLOBAL namespace
// (verified in the package's Source.cs), so it must not be wrapped in a namespace here.
internal sealed class AutoConstructorAttribute : Attribute { }

// A same-named attribute in a NON-global namespace: it is NOT the AutoConstructor marker and must not be excluded.
namespace Foo
{
    internal sealed class AutoConstructorAttribute : Attribute { }
}

namespace Tests.Diagnostics
{
    // Reproducer: https://sonarsource.atlassian.net/browse/NET-1541
    // The [AutoConstructor] source generator emits a constructor that assigns readonly fields and get-only
    // auto-properties. That generated code is not visible during analysis, so without the exclusion these
    // members would be wrongly reported as unassigned.
    [AutoConstructor]
    public partial class Repro_NET1541
    {
        private int Value { get; } // Compliant - assigned by the constructor generated by [AutoConstructor]
        private int field;         // Compliant - assigned by the constructor generated by [AutoConstructor]

        public int Read() => Value + field;
    }

    // Same shape without [AutoConstructor]: the members are still reported, proving the exclusion is attribute-specific.
    public partial class Repro_NET1541_NoAttribute
    {
        private int Value { get; } // Noncompliant {{Remove unassigned auto-property 'Value', or set its value.}}
        private int field;         // Noncompliant {{Remove unassigned field 'field', or set its value.}}

        public int Read() => Value + field;
    }

    // A same-named attribute from another namespace must NOT be treated as the AutoConstructor marker.
    [Foo.AutoConstructor]
    public partial class Repro_NET1541_WrongNamespace
    {
        private int Value { get; } // Noncompliant {{Remove unassigned auto-property 'Value', or set its value.}}
        private int field;         // Noncompliant {{Remove unassigned field 'field', or set its value.}}

        public int Read() => Value + field;
    }

    // Nested types: the exclusion must apply to the member's own (possibly nested) containing type.
    public partial class Outer
    {
        [AutoConstructor]
        private partial class InnerWithAttribute
        {
            private int Value { get; } // Compliant - nested [AutoConstructor] type is skipped
            private int field;         // Compliant - nested [AutoConstructor] type is skipped

            public int Read() => Value + field;
        }

        // Control: nested type without the attribute is still analyzed, proving nested members are reported normally.
        private class InnerWithoutAttribute
        {
            private int Value { get; } // Noncompliant {{Remove unassigned auto-property 'Value', or set its value.}}
            private int field;         // Noncompliant {{Remove unassigned field 'field', or set its value.}}

            public int Read() => Value + field;
        }
    }
}
