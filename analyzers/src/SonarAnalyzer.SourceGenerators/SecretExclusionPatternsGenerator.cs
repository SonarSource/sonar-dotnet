using System.Diagnostics.CodeAnalysis;
using System.Text;
using Newtonsoft.Json.Linq;

namespace SonarAnalyzer.SourceGenerators;

[Generator]
[ExcludeFromCodeCoverage]
public class SecretExclusionPatternsGenerator : IIncrementalGenerator
{
    public void Initialize(IncrementalGeneratorInitializationContext context)
    {
        // Only SonarAnalyzer.Core feeds the shared secret-patterns.json as an AdditionalFile (see SonarAnalyzer.Core.csproj), so the SecretExclusionPatterns catalog is emitted only there.
        var secretPatternsFiles = context.AdditionalTextsProvider
            .Combine(context.AnalyzerConfigOptionsProvider)
            .Where(x => x.Right.GetOptions(x.Left).TryGetValue("build_metadata.AdditionalFiles.SecretPatternsFile", out var value) && value == "true")
            .Select((x, _) => x.Left)
            .Collect();
        var source = secretPatternsFiles.Select((files, ct) => CancelableExecute(() => files.IsDefaultOrEmpty ? null : GenerateSource(files[0].GetText(ct)?.ToString()), ct));
        context.RegisterSourceOutput(source, (context, source) =>
            {
                if (source is not null)
                {
                    context.AddSource("SecretExclusionPatterns.g.cs", source);
                }
            });
    }

    private static T CancelableExecute<T>(Func<T> func, CancellationToken ct) =>
        ct.IsCancellationRequested ? default : func();

    private static string GenerateSource(string json)
    {
        if (string.IsNullOrWhiteSpace(json))
        {
            return null;
        }
        var root = JObject.Parse(json);
        var regexes = root["patternGroups"]?.SelectMany(x => x["patterns"]?.Values<string>() ?? []).ToArray() ?? [];
        var exactValues = root["exactMatchGroups"]?.SelectMany(x => x["values"]?.Values<string>() ?? []).ToArray() ?? [];
        var sb = new StringBuilder();
        sb.AppendLine("""
            // <auto-generated/>
            using System.Text.RegularExpressions;

            namespace SonarAnalyzer.Core.Common;

            // Generated by SecretExclusionPatternsGenerator from the shared secret-patterns.json (NET-4087). Do not edit by hand.
            // Regexes are applied case-insensitively with "find" (search-anywhere) semantics; exact values match in full, case-insensitively.
            internal static partial class SecretExclusionPatterns
            {
                public static readonly Regex[] Regexes =
                {
            """);
        foreach (var regex in regexes)
        {
            sb.AppendLine($"        CreateRegex({Encode(regex)}),");
        }
        sb.AppendLine("""
                };

                public static readonly ISet<string> ExactMatches = new HashSet<string>(StringComparer.OrdinalIgnoreCase)
                {
            """);
        foreach (var value in exactValues)
        {
            sb.AppendLine($"        {Encode(value)},");
        }
        sb.AppendLine("""
                };

                private static Regex CreateRegex(string pattern) =>
                    new(pattern, RegexOptions.IgnoreCase | RegexOptions.CultureInvariant, Constants.DefaultRegexTimeout);
            }
            """);
        return sb.ToString();
    }

    private static string Encode(string value) =>
        $@"@""{value.Replace(@"""", @"""""")}""";
}
